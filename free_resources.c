#include "push_swap.h"

void	free_split(char **arr)
{
	int	i;

	i = 0;
	while (arr[i])
	{
		free(arr[i]);
		i++;
	}
	free(arr);
}

void	free_resources(char *str, char **argv, t_list **a, t_list **b)
{
	free(str);
	free_split(argv);
	ft_lstclear(a);
	ft_lstclear(b);
}
